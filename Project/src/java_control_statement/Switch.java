package java_control_statement;

public class Switch {

	public static void main(String[] args) {
			String s="b";
			
			switch (s) {
			case"cd":
				System.out.println("value is cd");
				break;

			case"ef":
				System.out.println("value is ef");
				break;
			
			case"b":
			System.out.println("value is ab");
			break;
			
			default:
				System.out.println("all condition is true");
				break;
			}
			
			}
	}


