package java_control_statement;

public class Loop_statement {

	public static void main(String[] args) {
			for (int i = 1; i<4; i++) {
				System.out.println("the value of i is:-"+i);
			}
			
			for(int n = 10; n<=15; n++) {
				System.out.println(n);
			}
			
			for(int g=100; g>90; g--) {
				System.out.println(g);
			}

	}

}
