package operaters;

public class Comparision_operators {
			

	public static void main(String[] args) {
		int i = 20;
		int r = 200
				;
		    //equal to 
		 System.out.println(r==i);
		System.out.println(i==r);
		    
		    //not equal to
			System.out.println(i!=r);
			System.out.println(r!=i);
			
		    //less than
			System.out.println(i<r);
			System.out.println(r>i);
			
			//less than equal to
			System.out.println(i<=r);
			System.out.println(r<=i);
			
			//greater than
			System.out.println(i>r);
			System.out.println(r<i);
			
			//greater than equal to
			System.out.println(i>=r);
			System.out.println(r<=i);
			
	}

}
